//Navbar functions.

function openNav() {
  document.getElementById("hamburgerStack").style.display = "block";
  document.getElementById("paypal-button-container").style.display = "none";

}
function closeNav() {
  document.getElementById("hamburgerStack").style.display = "";
  document.getElementById("paypal-button-container").style.display = "block";
}
